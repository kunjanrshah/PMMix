package org.a0z.mpd.event;

/**
 * Represents a repeat state change event.
 *
 * @author Felipe Gustavo de Almeida
 * @version $Id: MPDRepeatChangedEvent.java 2614 2004-11-11 18:46:31Z galmeida $
 */
public class MPDRepeatChangedEvent {
    private boolean repeat;
    /**
     * Constructs a new <code>MPDRandomChangedEvent</code>.
     * @param connected new repeat state: true, on; false, off.
     */
    public MPDRepeatChangedEvent(boolean connected) {
        this.repeat = connected;
    }

    /**
     * Retrieves new repeat state.
     * @return true when repeat is on, false when repeat is off.
     */
    public boolean isRepeat() {
        return repeat;
    }
}
